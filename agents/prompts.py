OPPONENT_EMPTY = "[반대편 주장]\n아직 제공되지 않음. 먼저 주도적으로 당신의 시나리오를 펼치세요."


def opponent_block(side: str, last_argument: str) -> str:
    """반대편 주장을 프롬프트 블록으로 포맷."""
    if not last_argument:
        return OPPONENT_EMPTY
    label = "Bull Researcher" if side == "bull" else "Bear Researcher"
    return f"[반대편 주장 — {label}]\n{last_argument.strip()}"
